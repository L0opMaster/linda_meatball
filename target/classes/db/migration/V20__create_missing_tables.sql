CREATE TABLE customer_credit_accounts (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  customer_id BIGINT NOT NULL UNIQUE,
  credit_limit DECIMAL(18,2) NOT NULL DEFAULT 0,
  balance DECIMAL(18,2) NOT NULL DEFAULT 0,
  CONSTRAINT fk_cca_customer FOREIGN KEY (customer_id) REFERENCES customers(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE invoice_settings (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  prefix VARCHAR(20) NOT NULL DEFAULT 'INV',
  next_number BIGINT NOT NULL DEFAULT 1,
  footer VARCHAR(255),
  show_tax BOOLEAN NOT NULL DEFAULT TRUE,
  show_khqr BOOLEAN NOT NULL DEFAULT TRUE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
